# Add new element to list append function.

names = ['jaber','553','islam','454']
names.append('azharul') # Adding something with the append function always puts it at the end of the list
print(names)



#insert function.
names = ['jaber','553','islam','454']
names.insert(0,'cmt')# The new item can be added to any index using the insert function
print(names)



#extend function.
names = ['jaber','553','islam','454']
names.extend('azharul islam jaber')# Extend function is used to join multiple items together
print(names)